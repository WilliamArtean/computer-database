package com.excilys.mantegazza.cdb.dto;

public class CompanyDTO {

	private String name;
	private long id;
	
	public CompanyDTO() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}
	
}
